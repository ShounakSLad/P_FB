
public class Break {

	public static void main(String[] args) {
		
		
		
		int num=40;
		switch (num) {
		
		case 0:
			System.out.println("number is 0");
			
			break;
			
		case 1:
			System.out.println("number is 1");
			
			break;
		
		case 12:                                         // 12 is for example
			System.out.println("number is 40");
			
			break;
			
		default:
			System.out.println(num);
		
		}
	
	}

}
